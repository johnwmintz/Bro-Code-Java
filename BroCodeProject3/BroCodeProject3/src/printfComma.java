public class printfComma {
    void main() {
        double number1 = 1000.75;
        double number2 = 4257.124;
        double number3 = 100000.25;

        System.out.printf("%,.2f\n", number1);
        System.out.printf("%,.2f\n", number2);
        System.out.printf("%,.2f\n", number3);
    }

}
